     * Store a newly created resource in storage.
     */
    public function store(StoreMemberRequest $request)
    {
        $existingMember = Member::where('user_id', '=',  $request->user_id)->count();

        if ($existingMember == 1) {
            return redirect('/')->with('error', 'Anda sudah terdaftar member');
        } else {
            Member::create($request->validated());
            return redirect('/')->with('success', 'Berhasil daftar member');
        }
